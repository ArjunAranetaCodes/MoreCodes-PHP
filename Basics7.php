//If, ElseIf, and Else Structure

<?php
 $num1 = 1;
 $num2 = 2;

 //if statement
 if(num1>num2){
  echo "First number is higher!<br/>";
 }

 //if-else statement
 if(num1==num2){
  echo "They are equal!<br/>";
 }else{
  echo "They are not equal!<br/>";
 }

 //if-else if statement
 if(num1>num2){
  echo "First number is greater!<br/>";
 }else if(num1<num2){
  echo "Second number is greater!<br/>";
 }
?>




